// EBNF 写法
// 1. 允许小数的四则运算表达式
四则运算表达式 = 加法算式;
加法算式 = (加法算式, ("+"|"="), 加法算式) | 乘法算式;
乘法算式 = (乘法算式, ("*"|"/"), 乘法算式) | 小数;
小数 = 整数, [".", {数字}];
整数 = ({非零数字}, {数字}) | 数字;
数字 = "0" | 非零数字;
非零数字 = "1"|"2"|"3"|"4"|"5"|"6"|"7"|"8"|"9";

// 2. 允许括号的四则运算表达式

四则运算表达式 = 加法算式;
加法算式 = (加法算式, ("+"|"="), 加法算式) | 乘法算式;
乘法算式 = (乘法算式, ("*"|"/"), 乘法算式) | 括号算式;
括号算式 = ("(", (加法算式|乘法算式|括号算式) ,")") | 整数;
整数 = ({非零数字}, {数字}) | 数字;
数字 = "0" | 非零数字;
非零数字 = "1"|"2"|"3"|"4"|"5"|"6"|"7"|"8"|"9";